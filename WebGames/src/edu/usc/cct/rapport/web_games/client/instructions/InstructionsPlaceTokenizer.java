package edu.usc.cct.rapport.web_games.client.instructions;

import com.google.gwt.place.shared.PlaceTokenizer;
import com.google.gwt.place.shared.Prefix;

import edu.usc.cct.rapport.web_games.client.WebGamesPlaceTokenizer;


@Prefix(InstructionsPlace.placeName)
public class InstructionsPlaceTokenizer extends WebGamesPlaceTokenizer implements PlaceTokenizer<InstructionsPlace> {

	@Override
	public String getToken(final InstructionsPlace place) {
		return super.getToken(place);
	};


	@Override
	public InstructionsPlace getPlace(final String token) {
		final InstructionsPlace result = placeFactory.createInstructionsPlace(token);
		return result;
	};

};
