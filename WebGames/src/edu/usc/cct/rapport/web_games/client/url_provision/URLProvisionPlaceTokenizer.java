package edu.usc.cct.rapport.web_games.client.url_provision;

import com.google.gwt.place.shared.PlaceTokenizer;
import com.google.gwt.place.shared.Prefix;

import edu.usc.cct.rapport.web_games.client.WebGamesPlaceTokenizer;

@Prefix(URLProvisionPlace.placeName)
public class URLProvisionPlaceTokenizer extends WebGamesPlaceTokenizer implements PlaceTokenizer<URLProvisionPlace> {

	@Override
	public String getToken(final URLProvisionPlace place) {
		return super.getToken(place);
	};


	@Override
	public URLProvisionPlace getPlace(final String token) {
		final URLProvisionPlace result = placeFactory.createURLProvisionPlace(token);
		return result;
	};

};
