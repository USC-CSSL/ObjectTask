package edu.usc.cct.rapport.web_games.client.specify_experiment_conditions;

import com.google.gwt.place.shared.PlaceTokenizer;
import com.google.gwt.place.shared.Prefix;

import edu.usc.cct.rapport.web_games.client.WebGamesPlaceTokenizer;

@Prefix(SpecifyExperimentConditionsPlace.placeName)
public class SpecifyExperimentConditionsPlaceTokenizer extends WebGamesPlaceTokenizer implements PlaceTokenizer<SpecifyExperimentConditionsPlace> {

	@Override
	public String getToken(final SpecifyExperimentConditionsPlace place) {
		return super.getToken(place);
	};


	@Override
	public SpecifyExperimentConditionsPlace getPlace(final String token) {
		final SpecifyExperimentConditionsPlace result = placeFactory.createSpecifyExperimentConditionsPlace(token);
		return result;
	};

};
