package edu.usc.cct.rapport.web_games.client.trading_area;

import edu.usc.cct.rapport.web_games.client.recording.DoublySerializable;

public class AllocationDS implements DoublySerializable {

	static final private long serialVersionUID = 1L;

	private AgentEnum agentMakingProposal;
	private TradingObjectEnum tradingObjectEnum;
	private Integer count;


	public AllocationDS() {};


	public void setAgentMakingProposal(final AgentEnum agentMakingProposal) {
		this.agentMakingProposal = agentMakingProposal;
	};


	public void setTradingObjectEnum(final TradingObjectEnum tradingObjectEnum) {
		this.tradingObjectEnum = tradingObjectEnum;
	};


	public void setCount(final Integer count) {
		this.count = count;
	};


	public AgentEnum getAgentMakingProposal() {
		return this.agentMakingProposal;
	};


	public TradingObjectEnum getTradingObjectEnum() {
		return this.tradingObjectEnum;
	};


	public Integer getCount() {
		return this.count;
	};

};
